// i18n Hook
export function useTranslation(lang: Language = 'zh') {
  const t = translations[lang];
  
  // æ›¿æ¢å ä½ç¬¦çš„è¾…åŠ©å‡½æ•°
  const replace = (text: string, params: Record<string, string | number>) => {
    let result = text;
    Object.keys(params).forEach(key => {
      result = result.replace(`{${key}}`, String(params[key]));
    });
    return result;
  };
  
  return { t, replace };
}
